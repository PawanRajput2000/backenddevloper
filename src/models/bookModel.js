const mongoose = require('mongoose');

const bookSchema = new mongoose.Schema( {
    bookName: String, 
    authorName: String, 
    tags: [String],
    
    isPublished: Boolean,
    prices: {
        indianPrice: String,
        europePrice: String,
    },
   year : {
    type : String,
    default : "2021"
   },
   totalPages : Number,
   stockAvailable : Boolean
}, { timestamps: true });


module.exports = mongoose.model('book', bookSchema) //users

